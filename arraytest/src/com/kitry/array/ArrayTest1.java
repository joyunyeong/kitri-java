package com.kitry.array;

public class ArrayTest1 {
	
	public static void main(String[] args) {
		int x1 = 10;
		int x2 = 20;
		int x3 = 30;
		System.out.println("x1 = " + x1 + "\nx2 = " + x2 + "\nx3 = " + x3);
		System.out.println("---------------------------------");
		
		int x[];
		x = new int[3];
		x[0] = 10;
		x[1] = 20;
		x[2] = 30;
		System.out.println("x1 = " + x[0] + "\nx2 = " + x[1] + "\nx3 = " + x[2]);
		System.out.println("---------------------------------");
		
	}
}
